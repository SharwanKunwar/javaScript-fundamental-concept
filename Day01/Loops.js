// For ----------------------
// A traditional loop with initialization, condition, and increment/decrement. Runs a block of code a fixed number of times

// while --------------------
// Repeats a block of code as long as the condition is true (checks before execution).

// do-while ----------------
// Similar to while, but always executes the block at least once (condition checked after execution).

// for-of ----------------
// Iterates over values of an iterable (like arrays, strings, maps, sets).

// forEach ----------------
// Array method that executes a function once for each element (cannot break early).

// for-in ----------------
// Iterates over enumerable property keys of an object (not recommended for arrays).

// Object.entries ----------------
// Returns an array of [key, value] pairs from an object, often used with for-of to loop through objects.